	<h2>How to Stop Plastic Pollution in the Ocean?</h2><br>
	<p>It is quite hard to retrieve plastic from the ocean once it has entered it. New technologies allow us to catch larger marine debris, but small plastic items and microplastics are virtually impossible to reach, especially when they are deep in the ocean.

<br><br>Therefore, many scientists and conservationists have declared that the best solution is to prevent plastic waste from entering rivers and seas in the first place.

<br><br>This could be accomplished with the improvement of our waste management systems and the implementation of recycling. In addition, it is essential to reconsider the design and usage of disposable packaging, and the reduction in manufacturing of unnecessary single-use plastics.</p>
